int patch_pid() {
    // 0xFFFF9004 always points to the current KProcess
    curr_kproc = *(u32*)0xFFFF9004;
    *(u32*)(curr_kproc + kproc_offset_pid) = 0;
    return 0;
}

int unpatch_pid() {
    *(u32*)(curr_kproc + kproc_offset_pid) = self_pid;
    return 0;
}

void reinit_srv() {
    srvExit();
    srvInit();
}

void patch_srv_access() {
	u8 isN3DS = 0;
	APT_CheckNew3DS(NULL, &isN3DS);
	kproc_offset_pid = isN3DS ? KPROC_OFFSET_PID_N3DS : KPROC_OFFSET_PID_O3DS;

    svcGetProcessId(&self_pid, 0xFFFF8001);
    printf("Current process id: %lu\n", self_pid);

    printf("Patching srv access...");
    arm11_kernel_execute(patch_pid);
    reinit_srv();

    u32 new_pid;
    svcGetProcessId(&new_pid, 0xFFFF8001);
    printf("%s\n", new_pid == 0 ? "succeeded!" : "failed!");

    // Cleanup; won't take effect until srv is reinitialized
    arm11_kernel_execute(unpatch_pid);
}

static Result AM_OpenTicket(Handle *fileHandle)
{
	Result ret = 0;
	u32 *cmdbuf = getThreadCommandBuffer();

	cmdbuf[0] = 0x08010000;

	if((ret = svcSendSyncRequest(amHandle))!=0) return ret;

	*fileHandle = cmdbuf[3];
	
	return (Result)cmdbuf[1];
}

static Result AM_TicketAbortInstall(Handle fileHandle)
{
	Result ret = 0;
	u32 *cmdbuf = getThreadCommandBuffer();

	cmdbuf[0] = 0x08020002;
	cmdbuf[1] = ((1 << 26) - 0x4000000) | 0x10;
	cmdbuf[2] = fileHandle;

	if((ret = svcSendSyncRequest(amHandle))!=0) return ret;
	
	return (Result)cmdbuf[1];
}

static Result AM_TicketFinalizeInstall(Handle fileHandle)
{
	Result ret = 0;
	u32 *cmdbuf = getThreadCommandBuffer();

	cmdbuf[0] = 0x08030002;
	cmdbuf[1] = ((1 << 26) - 0x4000000) | 0x10;
	cmdbuf[2] = fileHandle;

	if((ret = svcSendSyncRequest(amHandle))!=0) return ret;
	
	return (Result)cmdbuf[1];
}

static Result AM_SetCertificates(int count, const u8 *certs[4], u32 sizes[4])
{
	Result ret = 0;
	u32 *cmdbuf = getThreadCommandBuffer();

	cmdbuf[0] = 0x08190108;
	cmdbuf[1] = count > 0 ? sizes[0] : 0;
	cmdbuf[2] = count > 1 ? sizes[1] : 0;
	cmdbuf[3] = count > 2 ? sizes[2] : 0;
	cmdbuf[4] = count > 3 ? sizes[3] : 0;
	cmdbuf[5] = 16 * (count > 0 ? sizes[0] : 0) + 10;
	cmdbuf[6] = count > 0 ? certs[0] : 0;
	cmdbuf[7] = 16 * (count > 1 ? sizes[1] : 0) + 10;
	cmdbuf[8] = count > 1 ? certs[1] : 0;
	cmdbuf[9] = 16 * (count > 2 ? sizes[2] : 0) + 10;
	cmdbuf[10] = count > 2 ? certs[2] : 0;
	cmdbuf[11] = 16 * (count > 3 ? sizes[3] : 0) + 10;
	cmdbuf[12] = count > 3 ? certs[3] : 0;

	if((ret = svcSendSyncRequest(amHandle))!=0) return ret;
	
	return (Result)cmdbuf[1];
}

static Result AM_DeleteTitle_(u8 mediatype, u64 titleID)
{
  Result ret = 0;
  u32 *cmdbuf = getThreadCommandBuffer();

  cmdbuf[0] = 0x041000C0;
  cmdbuf[1] = mediatype;
  cmdbuf[2] = titleID & 0xffffffff;
  cmdbuf[3] = (u32)(titleID >> 32);

  if((ret = svcSendSyncRequest(amHandle))!=0) return ret;

  return (Result)cmdbuf[1];
}

static Result WriteTicket(Handle fileHandle, const char *data, uint32_t size)
{
	Result res;
	uint32_t offset;
	uint32_t written;

	offset = 0;
	while (size)
	{
		if ((res = FSFILE_Write(fileHandle, &written, offset, 
								data+offset, size, 0x00010001)) != 0)
		{
			return res;
		}
		offset += written;
		size -= written;
	}
	return 0;
}

static inline uint64_t swapLong(void *X)
{
	uint64_t x = *(uint64_t*) X;
	x = (x & 0x00000000FFFFFFFF) << 32 | (x & 0xFFFFFFFF00000000) >> 32;
	x = (x & 0x0000FFFF0000FFFF) << 16 | (x & 0xFFFF0000FFFF0000) >> 16;
	x = (x & 0x00FF00FF00FF00FF) << 8  | (x & 0xFF00FF00FF00FF00) >> 8;
	return x;
}

int main()
{
	Result res;
	DIR *dp;
	struct dirent *ep;
	FILE *file;
	char path[1024];
	char *buffer;
	size_t buffer_size;
	off_t size;
	Handle ticket;
	int success;
	int total;

	// Initialize services
	gfxInitDefault(); // graphics
	hbInit();
	buffer = NULL;
	buffer_size = 0;

	consoleInit(GFX_TOP, NULL);

	int tries = 5;
    while (!doARM11Hax() && tries-- > 0);
    patch_srv_access();

    printf("ARM11 service patched!\n\n");
	if ((res = srvGetServiceHandle(&amHandle, "am:net")) != 0)
	{
		printf("Error 0x%08X initializing am.\n", res);
		svcSleepThread(10000000000ULL);
		goto end;
	}
	if ((dp = opendir("sdmc:/tickets")) == NULL)
	{
		printf("Cannot find 'tickets' directory\n");
		goto error;
	}

	printf("Press A to install all tickets.\n");
  printf("Prexx X to uninstall titles.\n");
	printf("Press B to exit without installing.\n");

	while(aptMainLoop())
	{
		//exit when user hits B
		hidScanInput();
		if(keysHeld()&KEY_B)goto error_no_wait;
    if(keysHeld()&KEY_X)goto uninstall_titles;
		if(keysHeld()&KEY_A)goto install_tickets;

		//wait & swap
		gfxFlushBuffers();
		gfxSwapBuffersGpu();
		gspWaitForVBlank();
	}

uninstall_titles:
  printf("Uninstalling titles.\n");
  res = AM_DeleteTitle_(0, 0x0004001000020800LL);
  printf("AM_DeleteTitle: 0x%08X\n", res);
  res = AM_DeleteTitle_(0, 0x0004001000020E00LL);
  printf("AM_DeleteTitle: 0x%08X\n", res);
  res = AM_DeleteTitle_(0, 0x0004001000020700LL);
  printf("AM_DeleteTitle: 0x%08X\n", res);
  printf("Done.");
  goto error;

install_tickets:
	printf("Setting certificates.\n");
	u8 *certs[] = {ticket_cert, ticket_rootca};
	u32 sizes[] = {sizeof(ticket_cert), sizeof(ticket_rootca)};
	if ((res = AM_SetCertificates(2, certs, sizes)) != 0)
	{
		printf("Error setting certificates: 0x%08X\n", res);
		goto error;
	}

	success = 0;
	total = 0;
	while ((ep = readdir(dp)) != NULL)
	{
		//wait & swap
		gfxFlushBuffers();
		gfxSwapBuffersGpu();
		gspWaitForVBlank();

		printf("Found %s\n", ep->d_name);
		total++;
		snprintf(path, 1024, "%s/%s", "sdmc:/tickets", ep->d_name);
		if ((file = fopen(path,"rb")) == NULL)
		{
			printf("Failed to open %s\n", path);
			continue;
		}
		fseek(file, 0, SEEK_END);
		size = ftell(file);
		fseek(file, 0, SEEK_SET);
		if (size <= 0)
		{
			printf("Invalid ticket size: %d\n", size);
			fclose(file);
			continue;
		}
		if (size > buffer_size)
		{
			if ((buffer = realloc(buffer, size)) == NULL)
			{
				printf("No memory left\n");
				fclose(file);
				continue;
			}
		}
		if (fread(buffer, size, 1, file) < 1)
		{
			printf("Cannot read ticket\n");
			fclose(file);
			continue;
		}
		fclose(file);
		if (*(uint32_t*)buffer == 0x00010004)
		{
			printf("Installing %llX\n", swapLong(buffer+0x1DC));
		}
		else
		{
			printf("Installing %s\n", ep->d_name);
		}
		if ((res = AM_OpenTicket(&ticket)) != 0)
		//if ((res = AM9_InstallTikBegin()) != 0)
		{
			printf("Error opening ticket: %08X\n", res);
			continue;
		}
		if ((res = WriteTicket(ticket, buffer, size)) != 0)
		//if ((res = AM9_InstallTikWrite(buffer, size)) != 0)
		{
			printf("Error writing ticket: %08X\n", res);
			AM_TicketAbortInstall(ticket);
			//AM9_InstallTikAbort();
			continue;
		}
		if ((res = AM_TicketFinalizeInstall(ticket)) != 0)
		//if ((res = AM9_InstallTikFinish()) != 0)
		{
			printf("Error finalizing ticket: %08X\n", res);
			continue;
		}
		printf("Installed %s\n", path);
		success++;
	}

	printf("Installed %d/%d successfully.\n", success, total);
	printf("Finished.\n");

	// Exit services
	//returning from main() returns to hbmenu when run under ninjhax
error:
	gfxFlushBuffers();
	gfxSwapBuffersGpu();
	svcSleepThread(10000000000ULL);
error_no_wait:
	free(buffer);
	closedir(dp);
	svcCloseHandle(amHandle);
end:
	//closing all services even more so
	hbExit();
	gfxExit();
	return 0;
}
